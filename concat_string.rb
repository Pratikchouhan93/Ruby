
#there are Four ways to concatinate Strings.

#  + 
string1 = "welcome "+"to the "+"Ruby."
puts string1

# space
string2 = "welcome " "to the " "Ruby."
puts string2

# << 
string3 = "welcome "<<"to the "<<"Ruby."
puts string3

# .concat() method
string4 = "welcome ".concat("to the ").concat("Ruby.")
puts string4